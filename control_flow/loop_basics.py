# Learn for loop, while loop, range

# FOR LOOP with range
print("Counting from 1 to 5:")
for i in range(1, 6):
    print(i)

# Loop through a string
word = "Python"
print("Letters in word:")
for letter in word:
    print(letter)

# WHILE LOOP
print("While loop counting:")
count = 0
while count <5:
    print("Count is:", count)
    count += 1

# Loop through a list
fruits = ["apple", "banana", "cherry"]
print("Fruits list:")
for fruit in fruits:
    print(fruit)