# operators.py
# Arithmetic, comparison, logical ops

# Arithmetic
a = 10
b = 3

print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)
print("Floor Division:", a // b)
print("Modulus:", a % b)
print("Exponent:", a ** b)

# Comparison
print("Is a equal to b?", a == b)
print("Is a greater than b?", a > b)
print("Is a not equal to b?", a != b)

# Logical
x = True
y = False

print("AND:", x and y)
print("OR:", x or y)
print("NOT x:", not x)
